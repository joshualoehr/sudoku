var generatePuzzle = function(num) {
    $("p#solutionStatus").text("");

    var puzzleData = puzzles[num];
    for (var r = 1; r <= 9; r++) {
        for (var c = 1; c <= 9; c++) {
            var index = (r-1)*9 + (c-1);
            var value = puzzleData.charAt(index);
            $("td.row" + r + ".col" + c).text(value === "0" ? " " : value);
        }
    }
};
